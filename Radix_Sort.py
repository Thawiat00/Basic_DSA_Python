def radixSort(mylist):
    """
    Radix Sort:
    - ‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏ó‡∏µ‡∏•‡∏∞‡∏´‡∏•‡∏±‡∏Å‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç ‡∏à‡∏≤‡∏Å‡∏´‡∏•‡∏±‡∏Å‡∏ô‡πâ‡∏≠‡∏¢ ‚Üí ‡∏°‡∏≤‡∏Å
    - ‡πÉ‡∏ä‡πâ buckets 0-9
    """
    print(f"üéØ Original array: {mylist}")
    
    # ‡∏™‡∏£‡πâ‡∏≤‡∏á 10 buckets (0-9)
    radixArray = [[], [], [], [], [], [], [], [], [], []]
    
    # ‡∏´‡∏≤‡∏Ñ‡πà‡∏≤‡∏™‡∏π‡∏á‡∏™‡∏∏‡∏î
    maxVal = max(mylist)
    exp = 1  # ‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ó‡∏µ‡πà‡∏´‡∏•‡∏±‡∏Å‡∏´‡∏ô‡πà‡∏ß‡∏¢ (10^0)
    digit_position = 1
    
    # ‡∏ß‡∏ô‡∏ï‡∏≤‡∏°‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏´‡∏•‡∏±‡∏Å
    while maxVal // exp > 0:
        print(f"\n{'='*60}")
        print(f"üî¢ Sorting digit position: {digit_position} (10^{digit_position-1})")
        print(f"{'='*60}")
        
        # ‡∏ß‡∏≤‡∏á values ‡∏•‡∏á buckets ‡∏ï‡∏≤‡∏°‡∏´‡∏•‡∏±‡∏Å‡∏ó‡∏µ‡πà‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏î‡∏π
        while len(mylist) > 0:
            val = mylist.pop()
            radixIndex = (val // exp) % 10  # ‡∏´‡∏≤‡∏´‡∏•‡∏±‡∏Å‡∏ó‡∏µ‡πà‡∏ï‡πâ‡∏≠‡∏á‡∏Å‡∏≤‡∏£
            radixArray[radixIndex].append(val)
        
        # ‡πÅ‡∏™‡∏î‡∏á buckets
        print("\nüì¶ Buckets:")
        for i, bucket in enumerate(radixArray):
            if bucket:
                print(f"   [{i}]: {bucket}")
        
        # ‡πÄ‡∏≠‡∏≤‡∏Å‡∏•‡∏±‡∏ö‡∏°‡∏≤‡πÉ‡∏™‡πà mylist
        for bucket in radixArray:
            while len(bucket) > 0:
                val = bucket.pop()
                mylist.append(val)
        
        print(f"\nüìä Array after sorting digit {digit_position}: {mylist}")
        
        exp *= 10  # ‡πÑ‡∏õ‡∏´‡∏•‡∏±‡∏Å‡∏ñ‡∏±‡∏î‡πÑ‡∏õ
        digit_position += 1
    
    return mylist


